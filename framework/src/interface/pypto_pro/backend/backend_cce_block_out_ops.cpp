// mrgsort2 (format2): Multi-list merge sort for CCE backend
static std::string MakeBlockOutMrgsort2CCE(const ir::CallPtr& op, codegen::CodegenBase& codegen_base)
{
    auto& codegen = dynamic_cast<codegen::CCECodegen&>(codegen_base);
    size_t num_args = op->args_.size();
    CHECK(num_args >= 4 && num_args <= 6) << "block.mrgsort2: expected 4-6 args, got " << num_args;

    bool exhausted = op->GetKwarg<bool>("exhausted");
    std::string exhausted_str = exhausted ? "true" : "false";

    std::string dst = codegen.GetExprAsCode(op->args_[0]);
    std::string src0 = codegen.GetExprAsCode(op->args_[1]);
    std::string tmp = codegen.GetExprAsCode(op->args_[2]);
    std::string src1 = codegen.GetExprAsCode(op->args_[3]);

    // TMRGSORT requires exhausted as a non-deducible bool template parameter.
    // Use TMRGSORT_IMPL with decltype() to avoid spelling out all tile types explicitly.
    auto emit_tmrgsort = [&](const std::string& srcs_decl, const std::string& srcs_args) {
        std::ostringstream oss;
        oss << "MrgSortExecutedNumList executedNumList;";
        codegen.Emit(oss.str());
        oss.str("");
        oss << "TMRGSORT_IMPL<decltype(" << dst << "), decltype(" << tmp << "), " << srcs_decl << ", " << exhausted_str
            << ">(" << dst << ", executedNumList, " << tmp << ", " << srcs_args << ");";
        codegen.Emit(oss.str());
    };

    if (num_args == 4) {
        emit_tmrgsort("decltype(" + src0 + "), decltype(" + src1 + ")", src0 + ", " + src1);
    } else if (num_args == 5) {
        std::string src2 = codegen.GetExprAsCode(op->args_[4]);
        emit_tmrgsort("decltype(" + src0 + "), decltype(" + src1 + "), decltype(" + src2 + ")",
                      src0 + ", " + src1 + ", " + src2);
    } else {
        std::string src2 = codegen.GetExprAsCode(op->args_[4]);
        std::string src3 = codegen.GetExprAsCode(op->args_[5]);
        emit_tmrgsort("decltype(" + src0 + "), decltype(" + src1 + "), decltype(" + src2 + "), decltype(" + src3 + ")",
                      src0 + ", " + src1 + ", " + src2 + ", " + src3);
    }
    return "";
}

REGISTER_BACKEND_OP(BackendCCE, "block.mrgsort2")
    .set_pipe(ir::PipeType::V)
    .f_codegen([](const ir::CallPtr& op, codegen::CodegenBase& codegen) {
        return MakeBlockOutMrgsort2CCE(op, codegen);
    });

REGISTER_BACKEND_OP(BackendCCE, "block.histogram")
    .set_pipe(ir::PipeType::V)
    .f_codegen([](const ir::CallPtr& op, codegen::CodegenBase& codegen_base) {
        auto& codegen = dynamic_cast<codegen::CCECodegen&>(codegen_base);
        CHECK(op->args_.size() == 3) << "block.histogram: expected 3 args (dst, src, idx), got " << op->args_.size();

        std::string dst = codegen.GetExprAsCode(op->args_[0]);
        std::string src = codegen.GetExprAsCode(op->args_[1]);
        std::string idx = codegen.GetExprAsCode(op->args_[2]);

        bool is_msb = op->GetKwarg<bool>("is_msb");

        // Detect src dtype: if any arg is UINT16 we're in uint16 mode, otherwise uint32.
        bool has_uint16 = false;
        for (const auto& arg : op->args_) {
            if (auto tt = ir::As<ir::TileType>(arg->GetType())) {
                if (tt->dtype_ == ir::DataType::UINT16) {
                    has_uint16 = true;
                    break;
                }
            }
        }
        // UINT16: BYTE_1 (bits 15-8, MSB) or BYTE_0 (bits 7-0, LSB)
        // UINT32: BYTE_3 (bits 31-24, MSB, first pass) or BYTE_0 (bits 7-0, LSB, last pass)
        std::string byte_enum = has_uint16 ? (is_msb ? "HistByte::BYTE_1" : "HistByte::BYTE_0") :
                                             (is_msb ? "HistByte::BYTE_3" : "HistByte::BYTE_0");
        codegen.Emit("THISTOGRAM<" + byte_enum + ">(" + dst + ", " + src + ", " + idx + ");");
        return "";
    });

